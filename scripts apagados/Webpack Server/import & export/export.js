//------------------------------------=>Atraves desta sintaxe podemos importar estruturas de informações (functions, arrays, objects, etc...)
//------------------------------------=>Indicando a key-word export
//------------------------------------=>E criando a estrutra de informação desejada neste caso uma function
export  function soma(a, b){return a + b}

export  function divs(a, b){return a / b}

export  function multiply(a, b){return a * b}

export  function subtracao(a, b){return a - b}
